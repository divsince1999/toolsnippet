export function getToolBySlug(slug: string) {
  return tools.find((tool) => tool.slug === slug);
}

export function getRelatedTools(slug: string, count = 4) {
  const currentTool = getToolBySlug(slug);
  if (!currentTool) return tools.slice(0, count);

  // 1. Get tools from the same category
  const sameCategory = tools.filter(
    (tool) => tool.category === currentTool.category && tool.slug !== slug
  );

  // 2. Get tools from other categories
  const otherCategories = tools.filter(
    (tool) => tool.category !== currentTool.category && tool.slug !== slug
  );

  // 3. Shuffle both arrays to provide variety
  const shuffledSame = sameCategory.sort(() => 0.5 - Math.random());
  const shuffledOther = otherCategories.sort(() => 0.5 - Math.random());

  // 4. Combine them, prioritizing same category
  const combined = [...shuffledSame, ...shuffledOther];

  // 5. Return the requested number
  return combined.slice(0, count);
}

